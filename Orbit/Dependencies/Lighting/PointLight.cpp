	void Orbit::PointLight::Init()
	{
        glGenVertexArrays(1, &VAO);
        glGenBuffers(1, &VBO);

        glBindBuffer(GL_ARRAY_BUFFER, VBO);
        glBufferData(GL_ARRAY_BUFFER, this->vertices.size() * sizeof(float), &(this->vertices)[0], GL_STATIC_DRAW);

        glBindVertexArray(VAO);

        glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
        glEnableVertexAttribArray(0);

        glBindVertexArray(0);
        glBindBuffer(GL_ARRAY_BUFFER, 0);
	}

    void PointLight::Render()
    {
        glm::mat4 projection = glm::perspective(glm::radians(45.0f), (float)1920.0f / (float)1080.0f, 0.1f, 100.0f);

        glm::mat4 view = (*camera).GetViewMatrix();

        this->shader.Use();
        this->shader.SetMat4("projection", projection);

        this->shader.SetMat4("view", view);
        glm::mat4 model = glm::mat4(1.0f);
        model = glm::translate(model, this->position);
        model = glm::scale(model, glm::vec3(0.5f)); // a smaller cube
        float angle = 20.0f;
        model = glm::rotate(model, glm::radians(angle), glm::vec3(1.0f, 0.3f, 0.5f));
        this->shader.SetMat4("model", model);

        glBindVertexArray(VAO);
        
        glDrawArrays(GL_TRIANGLES, 0, 36);
    }
